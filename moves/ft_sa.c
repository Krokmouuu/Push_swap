#include "../push_swap.h"

int	ft_sa(t_list *a, int y)
{
	int	tmp;

	if (a->len <= 1)
		return (0);
	tmp = a->nbr[0];
	a->nbr[0] = a->nbr[1];
	a->nbr[1] = tmp;
	if (y == 1)
		printmove("sa");
	return (0);
}
