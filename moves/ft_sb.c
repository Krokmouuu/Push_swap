#include "../push_swap.h"

int	ft_sb(t_list *b, int y)
{
	int	tmp;

	if (b->len <= 1)
		return (0);
	tmp = b->nbr[0];
	b->nbr[0] = b->nbr[1];
	b->nbr[1] = tmp;
	if (y == 1)
		printmove("sb");
	return (0);
}
